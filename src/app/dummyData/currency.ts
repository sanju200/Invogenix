export function getCurrencySymbol(currencyCode: string, fallback?: string): string {
    if (!currencyCode) {
        return fallback || '$';
    }

    const upperCode = currencyCode.toString().toUpperCase().trim();
    return CURRENCY_SYMBOLS[upperCode] || fallback || upperCode;
}

